//JQuery used for hover effect on Nav Links
$('.liNavLinks').on('mouseenter',function(){
    $(this).css('backgroundColor','#e9d985');
    }
);

$('.liNavLinks').on('mouseleave',function(){
    $(this).css('backgroundColor','#222e50');
    }
);


